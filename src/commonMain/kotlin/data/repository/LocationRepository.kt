    fun getAllProvinces(): List<Province> = provinces

    fun getCitiesByProvince(provinceCode: String): List<City> {
        return provinces.find { it.code == provinceCode }?.cities ?: emptyList()
    }

    fun getProvinceByCode(code: String): Province? {
        return provinces.find { it.code == code }
    }

    // 获取字母索引（按省份拼音首字母）
    fun getIndexLetters(): List<Char> {
        return ('A'..'Z').toList()
    }

    // 根据字母获取省份
    fun getProvincesByLetter(letter: Char): List<Province> {
        // 简化的拼音映射
        val pinyinMap = mapOf(
            'A' to listOf("安徽省"),
            'B' to listOf("北京市", "重庆市"),
            'C' to listOf("重庆市"),
            'F' to listOf("福建省"),
            'G' to listOf("甘肃省", "广东省", "广西壮族自治区", "贵州省"),
            'H' to listOf("海南省", "河北省", "河南省", "黑龙江省", "湖北省", "湖南省"),
            'J' to listOf("吉林省", "江苏省", "江西省"),
            'L' to listOf("辽宁省"),
            'N' to listOf("内蒙古自治区", "宁夏回族自治区"),
            'Q' to listOf("青海省"),
            'S' to listOf("山东省", "山西省", "陕西省", "上海市", "四川省"),
            'T' to listOf("天津市", "西藏自治区"),
            'X' to listOf("新疆维吾尔自治区"),
            'Y' to listOf("云南省"),
            'Z' to listOf("浙江省")
        )
        val provinceNames = pinyinMap[letter] ?: emptyList()
        return provinces.filter { it.name in provinceNames }
    }
